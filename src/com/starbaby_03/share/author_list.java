package com.starbaby_03.share;

public class author_list {
	public String url;
	public String name;
	public String msg;
	public String time;
	public String authorId;
	public author_list(String url, String name, String msg, String time,String authorId) {
		super();
		this.url = url;
		this.name = name;
		this.msg = msg;
		this.time = time;
		this.authorId = authorId;
	}
	
}
